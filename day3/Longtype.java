package day3;

public class Longtype {
	public static void main(String [] args) {
		long num1 =Long.parseLong (args[0]);
		long num2 = Long.parseLong (args[1]);
		
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		
		
	}

}
